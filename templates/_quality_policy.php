<h2>&nbsp;&nbsp;&nbsp;MICRONCLEAN QUALITY POLICY</h2>
<ol>
<li>Micronclean is <b>committed</b> to total customer satisfaction in micron-contamination services of barrier and accessories. This will be achieved through delivery of services and products that meet customer quality expectations.</li>
<li>Micronclean ensures quality integrity through diligent <b>continual improvement</b> of process and product quality control measures, and achieves customer satisfaction by being prompt and effective in resolving customers' quality concerns.</li>
</ol>


<h2>&nbsp;&nbsp;&nbsp;MICRONCLEAN QUALITY OBJECTIVES</h2>
<ol>
<li>Retraining of our employees to improve and add new skills so that there is optimization and creativity in the work flows.</li>
<li>Lab equipments linearity to be established. Ask Vendor to perform best line fit on our Ion Chromatography.</li>
<li>Focus on Sterilization and Microbiological validation of our garments.  </li>
<li>Data Mining and Cost analysis tools to be implemented so that Income vs Expense tracking can be done online and real time. This gives better visibility for Forecast in commercial projects. </li>

